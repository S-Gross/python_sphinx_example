class Math02:
    """
    This math class contains the functions for  multiplication, and division.
    """

    def multi(arg1, arg2):
        """This is the multiplication function
        
        The multi function multiplicates two float numbers
            		
        Args:
            arg1 (float): Factor 1
            arg2 (float): Factor 2
        
        Return:
            float: Arg1 multiplied with arg2
        """
        return arg1 * arg2
        
        
    def div(arg1, arg2):
        """This is the division function
        
        The div function devieds the secend argument by the first
            		
        Args:
            arg1 (float): quotient
            arg2 (float): divisor
        
        Return:
            float: arg1 devieded by args
        """
        return arg1 / arg2        
        