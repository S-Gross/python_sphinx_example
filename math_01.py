class Math01:
    """
    This math class contains the functions for add and substraction
    """

    def add(arg1, arg2):
        """This is the addition function
        
        The add function adds two integer numbers
            		
        Args:
            arg1 (float): Description of arg1
            arg2 (float): Description of arg2
        
        Return:
            float: Sum of arg1 and arg2
        """
        return arg1 + arg2
        
        
    def sub(arg1, arg2):
        """This is the substraction function
        
        The sub function substracts the secton argument from the first
            		
        Args:
            arg1 (float): Description of arg1
            arg2 (float): Description of arg2
        
        Return:
            float: Arg1 minus arg2
        """
        return arg1 - arg2        
        